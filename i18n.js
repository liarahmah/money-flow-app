export function applyLanguage(langCode) {
    const dict = translations[langCode];
    if (!dict) return;

    // Handle RTL for Arabic
    if (langCode === 'ar') {
        document.documentElement.setAttribute('dir', 'rtl');
        document.documentElement.setAttribute('lang', 'ar');
    } else {
        document.documentElement.removeAttribute('dir');
        document.documentElement.setAttribute('lang', langCode);
    }

    // Replace text for all elements with data-i18n
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (dict[key]) {
            // Keep inner elements like icons if present (e.g. <i class="ph..."></i> text)
            // But doing direct innerText will wipe icons. 
            // Most of our HTML has <i class="..."></i> then Text Node.
            // Better to find the Text Node and replace it, or just wrap text in <span> tags in HTML.
            // Let's assume we've wrapped the translatable text in spans in HTML.
            el.innerText = dict[key];
        }
    });
}
